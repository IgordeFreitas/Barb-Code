class Agenda():
    def __init__(self, dataHora, status):
        self.dataHora = dataHora,
        self. status = status,

    def getDataHora (self):
        return self.dataHora
    
    def setDataHora(self, novaDataHora):
        self.dataHora = novaDataHora

    def getPreco(self):
        return self.status

    def setPreco(self, novoStatus):
        self.status = novoStatus